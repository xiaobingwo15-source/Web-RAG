-- IE Industrial Technology mock data tables
-- Matches mock_data/sales-data.csv and mock_data/employee-directory.xlsx

-- Sales data table
CREATE TABLE IF NOT EXISTS public.ie_sales (
  id serial PRIMARY KEY,
  quarter text NOT NULL,
  region text NOT NULL,
  product text NOT NULL,
  units_sold int NOT NULL,
  unit_price_rmb numeric(10,2) NOT NULL,
  revenue_rmb numeric(12,2) NOT NULL,
  profit_margin_pct numeric(5,2) NOT NULL,
  key_customer text
);

-- Employee directory table
CREATE TABLE IF NOT EXISTS public.ie_employees (
  id serial PRIMARY KEY,
  employee_id text NOT NULL UNIQUE,
  name text NOT NULL,
  department text NOT NULL,
  title text NOT NULL,
  location text NOT NULL,
  email text,
  phone text,
  hire_date date,
  manager text,
  salary_rmb numeric(10,2)
);

